    private void BuscarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarjButtonActionPerformed
        String buscar = TextoBuscarjTextField.getText();

        // limpiando datos actuales
        int filas = modelo.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);
        }

        // obteniendo y agregando los resultados del filtro
        String[] data = new String[3];
        for(Integrante i: lider.getArea().getIntegrantes(buscar)){
            data[0] = i.getNombre();
            data[1] = i.getEdad();
            data[2] = i.getDni();
            modelo.addRow(data);
        }

    }//GEN-LAST:event_BuscarjButtonActionPerformed

    private void VerDetallejButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_VerDetallejButtonActionPerformed
        int fila = TablaGestor.getSelectedRow();
        if (fila >= 0) {

            String DNI = modelo.getValueAt(fila, 2).toString();
            Integrante integrante = lider.getArea().getIntegrante(DNI);

            if (integrante != null) {

                Detalle detalle = new Detalle(integrante);
                detalle.setVisible(true);
                detalle.addWindowListener(new WindowAdapter() {

                    @Override
                    public void windowClosing(WindowEvent e) {
                        DetalleCerrando(detalle, DNI);
                    }
                });
                this.setVisible(false);
            }
        }
    }// GEN-LAST:event_VerDetallejButtonActionPerformed

    private void AgregarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarjButtonActionPerformed
        Detalle detalle = new Detalle(null);
        detalle.setVisible(true);
        detalle.addWindowListener(new WindowAdapter() {

            @Override
            public void windowClosing(WindowEvent e) {
                DetalleCerrando(detalle, null);
            }
        });
        this.setVisible(false);
    }//GEN-LAST:event_AgregarjButtonActionPerformed

    private void DetalleCerrando(Detalle detalle, String DNI) {
        int evento = detalle.getEvento();

        switch (evento) {
            case 1: // boton Modificar
                lider.getArea().modificarIntegrante(DNI, detalle.getIntegrante());

                CargarDatos();
                break;
            case 2: // boton Eliminar
                lider.getArea().eliminarIntegrante(DNI);

                CargarDatos();
                break;
            case 3: // boton Agregar
                lider.getArea().guardarIntegrante(detalle.getIntegrante());

                CargarDatos();
                break;
        }
        this.setVisible(true);
    }

    private void CargarDatos() {
        // Recargar lista de integrantes
        // Limpiar
        int filas = modelo.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);
        }

        // Cargando datos
        String[] data = new String[3];
        for (Integrante i : lider.getArea().getIntegrantes()) {
            data[0] = i.getNombre();
            data[1] = i.getEdad();
            data[2] = i.getDni();
            modelo.addRow(data);
        }
    }

